void gameOfLife(vector<vector<int>>& board) {
    int m = board.size(), n = board[0].size();
    vector<vector<int>> next = board;

    for(int i = 0; i < m; i++) {
        for(int j = 0; j < n; j++) {
            auto getAt = [&](int x, int y) {
                int wrapX = (m + (x % m)) % m;
                int wrapY = (n + (y % n)) % n;
                return board[wrapX][wrapY];
            };
            int live = 
            getAt(i - 1, j - 1) + getAt(i - 1, j) + getAt(i - 1, j + 1) +
            getAt(i, j - 1)     +                   getAt(i, j + 1)     +
            getAt(i + 1, j - 1) + getAt(i + 1, j) + getAt(i + 1, j + 1);

            if(board[i][j]) {
                if(live < 2)      next[i][j] = 0;
                else if(live > 3) next[i][j] = 0;
            } else if(!board[i][j])
                if(live == 3)     next[i][j] = 1;
        }
    }
    board = next;
}
void printBoard(vector<vector<int>>& board) {
    string buffer = "";
    for(int i = 0; i < board.size(); i++) {
        for(int j = 0; j < board[0].size(); j++) 
            buffer += (board[i][j]? "@ " : ". ");
        buffer += "\n";
    }
    cout << buffer << flush;
}

